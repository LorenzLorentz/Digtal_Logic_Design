def main() -> None:
    bytes_out = []
    for code in range(256):
        ch = chr(code) if 0 <= code <= 0x7E else ""
        if code in GLYPHS_BY_CODE:
            bytes_out.extend(GLYPHS_BY_CODE[code])
        elif ch in GLYPHS:
            bytes_out.extend(GLYPHS[ch])
        elif 0x20 <= code <= 0x7E:
            bytes_out.extend(TOFU)   # printable but unhandled -> tofu
        else:
            bytes_out.extend(BLANK)

    assert len(bytes_out) == 4096

    OUT.parent.mkdir(parents=True, exist_ok=True)
    with OUT.open("w") as f:
        for b in bytes_out:
            f.write(f"{b:02x}\n")
    print(f"wrote {OUT} ({len(bytes_out)} bytes)")


if __name__ == "__main__":
    main()
