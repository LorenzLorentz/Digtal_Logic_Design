#!/usr/bin/env python3
"""
Generate rtl/frontend/fe_font.hex -- a 4096-byte 8x16 ASCII / sprite
font ROM consumed by fe_glyph_rom.sv via $readmemh.

Layout: 256 code points * 16 rows of 8-bit bitmap. Bit 7 (MSB) of each
byte is the leftmost pixel.

Each glyph is described in this script as 16 lines of 8 chars; '#' is
a set pixel, '.' or ' ' is clear. Override or extend GLYPHS to swap in
a different font without touching the SV side.

Run:
  python3 scripts/gen_font.py
"""

from pathlib import Path

OUT = Path(__file__).resolve().parent.parent / "rtl" / "frontend" / "fe_font.hex"


def parse(s: str) -> list[int]:
    """16 lines of 8 chars -> 16 bytes; '#' = 1, '.' or ' ' = 0."""
    rows = [r for r in s.split("\n") if r != ""]
    while len(rows) < 16:
        rows.append("")
    rows = rows[:16]
    out = []
    for r in rows:
        r = (r + "        ")[:8]
        b = 0
        for i, ch in enumerate(r):
            if ch == "#":
                b |= 1 << (7 - i)
        out.append(b)
    return out


